CREATE TABLE PELANGGAN
("ID_PELANGGAN" VARCHAR2(10),
"NAMA_PELANGGAN" VARCHAR2(50),
"NOMOR_TELEPON" VARCHAR2(12),
"ALAMAT" VARCHAR(250),
"JENIS_PELANGGAN" CHAR(20),
CONSTRAINT "PELANGGAN_PK" PRIMARY KEY ("ID_PELANGGAN")
USING INDEX ENABLE
);

CREATE TABLE PEGAWAI
("ID_KARYAWAN" VARCHAR2(10),
"NAMA_KARYAWAN" VARCHAR2(50),
"JABATAN" VARCHAR2(20),
CONSTRAINT "KARYAWAN_PK" PRIMARY KEY ("ID_KARYAWAN")
USING INDEX ENABLE
);

CREATE TABLE JENIS_BARANG
("ID_JENIS" VARCHAR2(10),
"NAMA_JENIS_BARANG" VARCHAR(30),
"HARGA" NUMBER,
CONSTRAINT "JENIS_PK" PRIMARY KEY ("ID_JENIS")
USING INDEX ENABLE
);

CREATE TABLE SUPPLIER
("ID_SUPPLIER" VARCHAR2(10),
"NAMA_SUPPLIER" VARCHAR2(50),
"NOMOR_TELEPON" VARCHAR(16),
"ALAMAT" VARCHAR(250),
CONSTRAINT "SUPPLIER_PK" PRIMARY KEY ("ID_SUPPLIER")
USING INDEX ENABLE
);

CREATE TABLE TRANSAKSI
("ID_TRANSAKSI" VARCHAR2(10),
"TANGGAL" DATE DEFAULT SYSDATE,
"TOTAL_BIAYA" NUMBER,
"METODE_PEMBAYARAN" VARCHAR(20) CHECK(METODE_PEMBAYARAN IN ('TRANSFER','TUNAI')),
"ID_PELANGGAN" VARCHAR2(10),
"ID_KARYAWAN" VARCHAR2(10),
CONSTRAINT "TRANSAKSI_PK" PRIMARY KEY ("ID_TRANSAKSI")
USING INDEX ENABLE
);

ALTER TABLE TRANSAKSI ADD CONSTRAINT "ID_PEL_FK1" FOREIGN KEY ("ID_PELANGGAN") REFERENCES PELANGGAN ("ID_PELANGGAN") ENABLE;

CREATE INDEX "PELANGGAN_IDX" ON TRANSAKSI ("ID_PELANGGAN");

ALTER TABLE TRANSAKSI ADD CONSTRAINT "ID_KARYAWAN_FK2" FOREIGN KEY ("ID_KARYAWAN") REFERENCES PEGAWAI ("ID_KARYAWAN") ENABLE;

CREATE INDEX "KARYAWAN_IDX" ON TRANSAKSI ("ID_KARYAWAN");

CREATE TABLE BARANG
("ID_BARANG" NUMBER,
"ID_JENIS" VARCHAR2(10),
"STATUS" VARCHAR2(25),
CONSTRAINT "BARANG_PK" PRIMARY KEY ("ID_BARANG")
USING INDEX ENABLE 
);

ALTER TABLE BARANG ADD CONSTRAINT "ID_JENIS_FK1" FOREIGN KEY ("ID_JENIS") REFERENCES JENIS_BARANG ("ID_JENIS") ENABLE;

CREATE INDEX "JENIS_IDX" ON BARANG ("ID_JENIS");

CREATE TABLE ITEM_BARANG
("JUMLAH_ITEM" NUMBER,
"ID_BARANG" NUMBER,
"ID_TRANSAKSI" VARCHAR2(10),
"ID_SUPPLIER" VARCHAR2(10),
CONSTRAINT "ITEM_PK" PRIMARY KEY ("ID_BARANG","ID_TRANSAKSI","ID_SUPPLIER")
USING INDEX ENABLE
);

ALTER TABLE ITEM_BARANG ADD CONSTRAINT "ID_SUPPLIER_FK2" FOREIGN KEY ("ID_SUPPLIER")
	REFERENCES SUPPLIER ("ID_SUPPLIER") ENABLE;


ALTER TABLE ITEM_BARANG ADD CONSTRAINT "ID_TRANSAKSI_FK2" FOREIGN KEY ("ID_TRANSAKSI")
	REFERENCES TRANSAKSI ("ID_TRANSAKSI") ENABLE;


ALTER TABLE ITEM_BARANG ADD CONSTRAINT "ID_BARANG_FK3" FOREIGN KEY ("ID_BARANG")
	REFERENCES BARANG ("ID_BARANG") ENABLE;

